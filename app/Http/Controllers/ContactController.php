    public function store(Request $request)
    {
        //
        $new_mssg = new Contact;
        $new_mssg ->name = $request->name;
        $new_mssg ->phone_number = $request->phone_number;
        $new_mssg ->email = $request->email;
        $new_mssg ->message = $request->message;
        $new_mssg->save();

        return redirect('/');
    }

